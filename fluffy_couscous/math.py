"""
A small set of functions for doing math operations.
"""

# Write a function named add taht adds two values

def add(a,b):
    """
    A function that adds two values
    :param a: first number
    :param b: second number
    :return: The values that adds a and b
    """
    return a + b

def mult(a,b):
    """
    A function that multiplies two values
    :param a: first number
    :param b: second number
    :return: The values that multiplies a and b
    """
    return a * b
